public class CameInfo {
    private String id;
    private String cameId;
    private String camLocaltion;
    private String camType;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCameId() {
        return cameId;
    }

    public void setCameId(String cameId) {
        this.cameId = cameId;
    }

    public String getCamLocaltion() {
        return camLocaltion;
    }

    public void setCamLocaltion(String camLocaltion) {
        this.camLocaltion = camLocaltion;
    }

    public String getCamType() {
        return camType;
    }

    public void setCamType(String camType) {
        this.camType = camType;
    }

}
